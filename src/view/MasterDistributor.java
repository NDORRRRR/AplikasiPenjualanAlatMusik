    private boolean validasiInput() {
    boolean valid = false;
  
     if (t_idDistributor.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(javax.swing.JOptionPane.getRootFrame(), "Kode Jenis Barang tidak boleh kosong");
    } else if (t_NamaDistributor.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(javax.swing.JOptionPane.getRootFrame(), "Nama Barang tidak boleh kosong");
    } else if (t_Telp.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(javax.swing.JOptionPane.getRootFrame(), "Nama Barang tidak boleh kosong");
    } else if (t_Alamat.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(javax.swing.JOptionPane.getRootFrame(), "Nama Barang tidak boleh kosong");
   
    } else {
        valid = true;
    }
    return valid;
}


