    private void dataTabel() {
        tampilData.setVisible(false);
        tambahData.setVisible(true);
        
        int row = tabel_barang.getSelectedRow();
        L_databarang2.setText("Perbarui Data Distributor");
        
        
        t_idDistributor.setEnabled(false);
        t_NamaDistributor.setEnabled(true);
        
        
        t_idDistributor.setText     (tabel_barang.getModel().getValueAt(row, 0).toString());
        t_NamaDistributor.setText   (tabel_barang.getModel().getValueAt(row, 1).toString());
        t_Telp.setText              (tabel_barang.getModel().getValueAt(row, 2).toString());
        t_Alamat.setText            (tabel_barang.getModel().getValueAt(row, 3).toString());
        
        
       
        bt_tambah1.setText("PERBARUI");
        btn_batal.setVisible(true);
    }

    private void hapusData() {
       int index = tabel_barang.getSelectedRow();
       if (index!=-1) {
           Model_distributor model = tblModel.getData(tabel_barang.convertRowIndexToModel(index));
           if (JOptionPane.showConfirmDialog
        (null, "Yakin data akan dihapus", "konfirmasi", JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
               servis.hapusData(model);
               tblModel.hapusData(index);
               loadData();
               resetForm();
           }
       }else{
           JOptionPane.showMessageDialog(null, "Pilih dahulu yang akan di update");
       }
    }

    private void tampilPanel() {
        mainPanel.removeAll();
        mainPanel.add(new MasterDistributor());
        mainPanel.repaint();
        mainPanel.revalidate();
    }

   
